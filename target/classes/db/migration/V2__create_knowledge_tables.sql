CREATE TABLE knowledge_document (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(255),
    content TEXT,
    index_status VARCHAR(20) DEFAULT 'PENDING',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chk_knowledge_document_index_status
        CHECK (index_status IN ('PENDING', 'INDEXING', 'INDEXED', 'FAILED'))
);

CREATE TABLE knowledge_document_chunk (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    document_id BIGINT,
    chunk_index INTEGER,
    chunk_text TEXT,
    embedding VECTOR(1536),
    embedding_model VARCHAR(255),
    CONSTRAINT fk_knowledge_document_chunk_document
        FOREIGN KEY (document_id) REFERENCES knowledge_document (id) ON DELETE CASCADE
);

CREATE INDEX idx_knowledge_document_chunk_document_id_chunk_index
    ON knowledge_document_chunk (document_id, chunk_index);
